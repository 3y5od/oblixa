export function buildPhaseClosurePayload() {
  if (AUTONOMOUS_PERF_PHASE_IDS.length !== new Set(AUTONOMOUS_PERF_PHASE_IDS).size) {
    throw new Error("AUTONOMOUS_PERF_PHASE_IDS contains duplicates");
  }
  const phases = {};
  for (const id of AUTONOMOUS_PERF_PHASE_IDS) {
    phases[id] = structuredClone(OVERRIDES[id] ?? DEFAULT_IN_REPO);
  }
  return {
    schemaVersion: 1,
    phases,
  };
}
